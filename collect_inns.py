def save_all_inns(filename: str = "inn_list.txt"):
    """Сохраняет все реальные ИНН в один файл."""
    all_inns = []
    for industry, inns in REAL_INNS.items():
        for inn in inns:
            if inn not in all_inns:
                all_inns.append(inn)
    
    with open(filename, "w", encoding="utf-8") as f:
        f.write("\n".join(all_inns))
    
    print(f"✅ Сохранено {len(all_inns)} уникальных ИНН в {filename}")
    return all_inns


def save_by_industry(output_dir: str = "inn_lists"):
    """Сохраняет ИНН по отраслям в отдельные файлы."""
    output_dir = Path(output_dir)
    output_dir.mkdir(exist_ok=True)
    
    total = 0
    for industry, inns in REAL_INNS.items():
        unique = list(dict.fromkeys(inns))
        filepath = output_dir / f"{industry}.txt"
        with open(filepath, "w", encoding="utf-8") as f:
            f.write("\n".join(unique))
        print(f"  📁 {industry}: {len(unique)} ИНН → {filepath}")
        total += len(unique)
    
    print(f"\n✅ Всего по отраслям: {total} ИНН в {output_dir}/")
    return total


def print_stats():
    """Статистика."""
    print("\n" + "=" * 60)
    print("  📊 250+ РЕАЛЬНЫХ ИНН КРУПНЕЙШИХ КОМПАНИЙ РФ")
    print("=" * 60)
    
    total = 0
    for industry, inns in REAL_INNS.items():
        unique = len(set(inns))
        total += unique
        bar = "█" * min(unique // 2, 30)
        print(f"  {industry:<20} {unique:>3} {bar}")
    
    print("-" * 60)
    print(f"  ВСЕГО реальных ИНН: {total}")
    print(f"  Источники: РБК-500, Forbes-2024, RAEX-600, ФНС")
    print("=" * 60)


